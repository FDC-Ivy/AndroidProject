package com.example.androidproject.Model;

public class Employees {
    private String mEmployeeID;
    private String mEmployeeSellerID;
    private String mEmployeeBranchID;

    public String getmEmployeeID() {
        return mEmployeeID;
    }

    public void setmEmployeeID(String mEmployeeID) {
        this.mEmployeeID = mEmployeeID;
    }

    public String getmEmployeeSellerID() {
        return mEmployeeSellerID;
    }

    public void setmEmployeeSellerID(String mEmployeeSellerID) {
        this.mEmployeeSellerID = mEmployeeSellerID;
    }

    public String getmEmployeeBranchID() {
        return mEmployeeBranchID;
    }

    public void setmEmployeeBranchID(String mEmployeeBranchID) {
        this.mEmployeeBranchID = mEmployeeBranchID;
    }
}
